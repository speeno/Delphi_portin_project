	  DEFAULT 0;
CREATE DOMAIN G6_GGEO_09_GRAT6 AS SMALLINT
	  DEFAULT 0;
CREATE DOMAIN G6_GGEO_10_GRAT7 AS SMALLINT
	  DEFAULT 0;
CREATE DOMAIN G6_GGEO_11_GRAT8 AS SMALLINT
	  DEFAULT 0;
CREATE DOMAIN G6_GGEO_12_GRAT9 AS SMALLINT
	  DEFAULT 0;
