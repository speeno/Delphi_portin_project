CREATE TRIGGER S1_SSUB_ID_NUM FOR S1_SSUB 
ACTIVE BEFORE INSERT POSITION 0
as
DECLARE VARIABLE Idmax INTEGER;
DECLARE VARIABLE Idmin INTEGER;
Begin
  IF (New.Gubun='Ăâ°í') Then Begin
  Select Max(Idnum) From S1_Ssub
  Where (S1_Ssub.Gdate = New.Gdate) AND (S1_Ssub.Scode = New.Scode) AND
        (S1_Ssub.Gcode = New.Gcode) AND (S1_Ssub.Jubun = New.Jubun) AND
        (S1_Ssub.Gjisa = New.Gjisa)
  INTO Idmax;
  if ((Idmax IS NULL)OR(Idmax = 0)) Then begin
    Select Max(Idnum) From S1_Ssub
    Where (S1_Ssub.Gdate = New.Gdate)
    INTO Idmin;
    if ((Idmin IS NULL)OR(Idmin = 0)) Then
    Idmax =(1);
    else
    Idmax =(Idmin+1);
  end else begin
    Idmax = Idmax;
  end
  New.Idnum = Idmax;
  End
  Else
  New.Idnum = 0;
End;

